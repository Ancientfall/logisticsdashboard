// Helper functions for vessel classification
const getVesselByName = (vesselName) => {
  if (!vesselName) return undefined;
  return vesselClassificationData.find(vessel => 
    vessel.vesselName.toLowerCase() === vesselName.toLowerCase()
  );
};

const getVesselsByCompany = (company) => {
  if (!company) return [];
  return vesselClassificationData.filter(vessel => 
    vessel.company.toLowerCase().includes(company.toLowerCase())
  );
};

const getVesselTypeFromName = (vesselName) => {
  const vessel = getVesselByName(vesselName);
  return vessel?.vesselType || 'Unknown';
};

const getVesselCompanyFromName = (vesselName) => {
  const vessel = getVesselByName(vesselName);
  return vessel?.company || 'Unknown';
};

const getVesselSizeFromName = (vesselName) => {
  const vessel = getVesselByName(vesselName);
  return vessel?.size || 0;
};

// Enhanced company inference using vessel classification
const inferCompanyFromVessel = (vesselName) => {
  if (!vesselName) return 'Unknown';
  
  // First try exact vessel classification lookup
  const vesselInfo = getVesselByName(vesselName);
  if (vesselInfo) {
    return vesselInfo.company;
  }
  
  // Fallback to pattern matching
  const name = vesselName.toLowerCase();
  if (name.includes('bp') || name.includes('british petroleum')) return 'BP';
  if (name.includes('shell')) return 'Shell';
  if (name.includes('chevron')) return 'Chevron';
  if (name.includes('exxon')) return 'ExxonMobil';
  if (name.includes('total')) return 'Total';
  if (name.includes('hos')) return 'Hornbeck Offshore';
  if (name.includes('harvey')) return 'Harvey Gulf';
  if (name.includes('edison') || name.includes('chouest')) return 'Edison Chouest Offshore';
  if (name.includes('fast')) return 'Edison Chouest Offshore'; // Fast vessels are Edison Chouest
  if (name.includes('candies')) return 'Otto Candies';
  if (name.includes('jackson')) return 'Jackson Offshore';
  if (name.includes('lab')) return 'Laborde Marine';
  if (name.includes('tidewater')) return 'Tidewater Marine';
  
  return 'Third Party';
};

